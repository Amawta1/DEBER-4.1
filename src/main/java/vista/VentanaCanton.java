    public void ActualizarTabla() {
        var header = new String[]{"Nombre", "Expancion Territorial", "Nro Habitantes", "Fundacion", "Provincia", "Codigo"};
        var data = new Object[this.cantonControl.listar().size()][7];
        for (var i = 0; i < this.cantonControl.listar().size(); i++) {
            data[i][0] = this.cantonControl.listar().get(i).getNombre();
            data[i][1] = this.cantonControl.listar().get(i).getExpancionTerritorial();
            data[i][3] = Integer.toString(this.cantonControl.listar().get(i).getYearFundacion().getYear()) + "-" + Integer.toString(this.cantonControl.listar().get(i).getYearFundacion().getMonthValue()) + "-" + Integer.toString(this.cantonControl.listar().get(i).getYearFundacion().getDayOfMonth());
            data[i][2] = Integer.toString((int) this.cantonControl.listar().get(i).getNroHabitantes());
            data[i][4] = this.cantonControl.listar().get(i).getProvincia().getNombre();
            data[i][5] = Integer.toString(this.cantonControl.listar().get(i).getCodigo());

        }

        this.tableModel = new DefaultTableModel(data, header);
        this.jTable1.setModel(this.tableModel);
    }

